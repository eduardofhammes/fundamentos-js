// função de soma, com prefixo de export, que permite a importacao no arquivo principal (index.js) e utilização dessa função
export function soma(num1, num2) {
    return num1 + num2
}

// função de subtração, com prefixo de export, que permite a importacao no arquivo principal (index.js) e utilização dessa função
export function subtracao(num1, num2) {
    return num1 - num2
}