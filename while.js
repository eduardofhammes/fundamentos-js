// prática de laçoes while e do-while

// contando até 10 com while
let contador = 1

while(contador <= 10){
    console.log('Número atual ->', contador)
    contador++
}

// contando até 10 com do-while
let numero = 1

do{
    console.log('número atual ->', numero)
    numero++
} while(numero <= 10)